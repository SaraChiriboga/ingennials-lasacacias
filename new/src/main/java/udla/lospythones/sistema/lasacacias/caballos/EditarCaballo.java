    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        // Verificar si el id existe
        String consultaExistencia = "SELECT * FROM caballo WHERE idcaballo = ?";
        try (PreparedStatement verificar = acacias.prepareStatement(consultaExistencia)) {
            verificar.setInt(1, id);
            ResultSet rs = verificar.executeQuery();

            if (rs.next()) {
                //modificación de base de datos
                String datos = "UPDATE caballo SET edad = ?, descripcion = ?, estadoSalud = ?, estadoEntrenamiento = ?, peso = ?, valorComercial = ?, campo = ?, alimentacion = ? WHERE idcaballo = ?";
                try (PreparedStatement editar = acacias.prepareStatement(datos)){
                    editar.setInt(1, Integer.parseInt(edad.getText()));
                    editar.setString(2, descripcion.getText());
                    editar.setString(3, estadoSalud.getText());
                    editar.setString(4, estadoEntrenamiento.getText());
                    editar.setDouble(5, Double.parseDouble(peso.getText()));
                    editar.setDouble(6, Double.parseDouble(valor.getText()));
                    editar.setString(7, campo.getText());
                    //se actualiza la cantidad de alimento en caso de cambios en el peso y edad del caballo
                    Caballo caballo = new Caballo();
                    double alimento = caballo.alimentacion(Integer.parseInt(edad.getText()), Double.parseDouble(peso.getText()), rs.getString("sexo"));
                    editar.setDouble(8, alimento);

                    // se añade el id en la instrucción
                    editar.setInt(9, id);

                    // se actualiza la tabla con los nuevos datos
                    editar.executeUpdate();
                    setVisible(false);
                    JOptionPane.showMessageDialog(null, "Información actualizada con éxito!");
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(null, "Ingrese datos válidos!");
                    
                }
            } else {
                JOptionPane.showMessageDialog(null, "No se encontraron datos con tal id...");
                id = Integer.parseInt(JOptionPane.showInputDialog(null, "ID del caballo:"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(EditarCaballo.class.getName()).log(Level.SEVERE, null, ex);
        }
  
    }//GEN-LAST:event_jButton1ActionPerformed

